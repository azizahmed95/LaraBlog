     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $user = Auth::user();
        $create_post = new Post();
        $user_id = $user->id;
        $post = $request->input();
        $create_post->user_id = $user_id;
        $create_post->category_id = '1';
        $create_post->title = $post['post_title'];
        $create_post->description = $post['post_desc'];
        $create_post->post_imgs = 'no-image.jpg';
        $create_post->save();
        return "Post created successfully!";

    }

